public class Calculate {
	// a call to square returns the square of the value passed. The method accepts an integer and returns an integer.
	public static int square(int number) {
		int answer=0;
		answer=number * number;
		return answer;
	}
	//a call to to cube returns the cube of the value passed The method accepts an integer and returns an integer.
	public static int cube(int number) {
		int answer=0;
		answer=number * number * number;
		return answer;			
    }
	// a call to average returns the average of the values passed to it. The method accepts two doubles and returns a double.
	public static int average(int number) {
    	int answer=0;
    	answer=number * number; 
    	return answer;
    }
 }

